# 論理積(AND) : `&`
#
# 例)
#   A: 1011 (11)
#   B: 0110 (6)
#   A & B => 0010 (2)
print(11 & 6)

# 論理和(OR) : `|`
#
# 例)
#   A: 1011 (11)
#   B: 0110 (6)
#   A & B => 1111 (15)
print(11 | 6)

# 排他的論理和(XOR) : `^`
#
# 例)
#   A: 1011 (11)
#   B: 0110 (6)
#   A & B => 1101 (13)
print(11 ^ 6)

# 左シフト (<<)
#
# 例)
#   11(1011) << 2 => 44 (101100)
print(11 << 2)

# 右シフト (>>)
#
# 例)
#   11(1011) >> 2 => 2 (10)
print(11 >> 2)
